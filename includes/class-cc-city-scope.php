    /**
     * Επιστρέφει τον σταθμό για έναν ΤΚ.
     *
     * @param string|int $tk ΤΚ (3-5 ψηφία)
     * @return string|null Κωδικός σταθμού ή null αν δεν βρεθεί
     */
    public static function get_station_for_postcode( $tk ) {
        $key = str_pad( (string) $tk, 5, '0', STR_PAD_LEFT );
        return isset( self::$tk_map[ $key ] ) ? self::$tk_map[ $key ] : null;
    }

    /**
     * Επιστρέφει 'ΕΝΤΟΣ ΠΟΛΗΣ' ή 'ΕΚΤΟΣ ΠΟΛΗΣ'.
     *
     * Ελέγχει αν υπάρχει κάποιο cluster που περιέχει ΚΑΙ τους δύο σταθμούς.
     *
     * @param string $origin_station Κωδικός σταθμού αποστολέα
     * @param string $dest_station   Κωδικός σταθμού παραλήπτη
     * @return string
     */
    public static function get_city_scope( $origin_station, $dest_station ) {
        $all_clusters = array_merge(
            array( self::ATHENS_CLUSTER, self::THESS_CLUSTER ),
            self::EXTRA_CLUSTERS
        );

        foreach ( $all_clusters as $cluster ) {
            if ( in_array( $origin_station, $cluster, true ) && in_array( $dest_station, $cluster, true ) ) {
                return 'ΕΝΤΟΣ ΠΟΛΗΣ';
            }
        }

        return 'ΕΚΤΟΣ ΠΟΛΗΣ';
    }

    /**
     * Επιλέγει αυτόματα BasicService 111 ή 211 για next_day αποστολές.
     *
     * @param string     $origin_station Κωδικός σταθμού αποστολέα (από setting cc_wc_shipper_station)
     * @param string|int $dest_postcode  ΤΚ παραλήπτη (από $order->get_billing_postcode())
     * @return array {
     *     @type string      $service_code  '111' ή '211'
     *     @type string      $scope         'ΕΝΤΟΣ ΠΟΛΗΣ' ή 'ΕΚΤΟΣ ΠΟΛΗΣ'
     *     @type string|null $dest_station  Κωδικός σταθμού παραλήπτη (null αν δεν βρεθεί ΤΚ)
     * }
     */
    public static function resolve_next_day_service( $origin_station, $dest_postcode ) {
        $dest_station = self::get_station_for_postcode( $dest_postcode );

        if ( null === $dest_station ) {
            // Άγνωστος ΤΚ → ασφαλής fallback σε ΕΚΤΟΣ
            return array(
                'service_code' => '211',
                'scope'        => 'ΕΚΤΟΣ ΠΟΛΗΣ',
                'dest_station' => null,
            );
        }

        $scope        = self::get_city_scope( $origin_station, $dest_station );
        $service_code = ( 'ΕΝΤΟΣ ΠΟΛΗΣ' === $scope ) ? '111' : '211';

        return array(
            'service_code' => $service_code,
            'scope'        => $scope,
            'dest_station' => $dest_station,
        );
    }
